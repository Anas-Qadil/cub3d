#include "cub3d.h"

void	draw_player(t_data *mlx)
{
	int	i;
	int	j;

	i = 0;
	while (i < 5)
	{
		j = 0;
		while (j < 5)
		{
			my_mlx_pixel_put_player(mlx, i, j, 0x00FF0000);
			j++;
		}
		i++;
	}
}
